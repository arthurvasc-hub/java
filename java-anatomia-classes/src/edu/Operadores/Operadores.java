package edu.Operadores;

public class Operadores {
    public static void main(String[] args) {
       int numero = 5;
       // Transformando o numero em negativo ->
       numero = - numero;
       System.out.println(numero);
        // Para transformar o numero novamente em positivo ->
       numero = numero * -1;
       System.out.println(numero);
    }
}
